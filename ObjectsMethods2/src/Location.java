public class Location {

	
	private int x;
	private int y;
	
	
	public Location( int x, int y )
	{
		this.x = x;
		this.y = y;
	}
	
	
	/** Returns the x-coordinate of the Location */
	public int getX()
	{
		return	this.x;
	}
	
	/** Returns the y-coordinate of the Location */
	public int getY()
	{
		return	this.y;
	}
	
}